package projekakhirkel4;

public class Akun {
    protected String nama, alamat, password, email;
    protected int id;

    public Akun(String nama, String alamat, String password, String email, int id) {
        this.nama = nama;
        this.alamat = alamat;
        this.password = password;
        this.email = email;
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
    
}
